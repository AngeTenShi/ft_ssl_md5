unsigned long long state[16] = {0};

void sub_bytes() {
	for (int i = 0; i < 16; i++) {
		unsigned long long temp = 0;
		for (int j = 0; j < 8; j++) {
			temp |= (unsigned long long)S[(state[i] >> (8 * (7 - j))) & 0xFF] << (8 * (7 - j));
		}
		state[i] = temp;
	}
}

void shift_columns() {
	for (int i = 0; i < 16; i++) {
		unsigned long long shifted = 0;
		for (int j = 0; j < 8; j++) {
			shifted |= ((state[(j + i) % 16] >> (8 * (7 - i))) & 0xFF) << (8 * (7 - j));
		}
		state[i] = shifted;
	}
}

void mix_rows() {
	unsigned long long temp[16];
	for (int i = 0; i < 16; i++) {
		temp[i] = 0;
		for (int j = 0; j < 8; j++) {
			temp[i] ^= M[i % 8][j] * state[j + (i / 8) * 8];
		}
	}
	for (int i = 0; i < 16; i++) {
		state[i] = temp[i];
	}
}

void add_round_key(unsigned long long *K) {
	for (int i = 0; i < 16; i++) {
		state[i] ^= K[i];
	}
}

void process_block_whirlpool(const char *input) {
	unsigned long long K[16];

	for (int i = 0; i < 16; i++) {
		K[i] = state[i] = ((unsigned long long *)input)[i];
	}

	add_round_key(K);

	for (int i = 0; i < 10; i++) {
		sub_bytes();
		shift_columns();
		mix_rows();
		add_round_key(K);
	}
}

char *whirlpool(const char *input, size_t len, size_t total_size) {
	char *hash = NULL;
	if (len % 128 != 0) {  // padding
		size_t new_len = len + 1;
		while (new_len % 128 != 112)
			new_len++;
		new_len += 16;  // pour les 128 bits de la taille du message
		char *msg = (char *)malloc(new_len);
		ft_memcpy(msg, input, len);
		msg[len] = 0x80;  // ajout du bit 1 Ã  la fin du message
		unsigned long long bit_len = total_size * 8;
		for (size_t i = 0; i < 16; i++)
			msg[new_len - 16 + i] = (bit_len >> (120 - i * 8)) & 0xFF;
		char *orig_msg = msg;
		while (new_len >= 128) {
			process_block_whirlpool(msg);
			msg += 128;
			new_len -= 128;
		}
		free(orig_msg);
	}
	while (len >= 128) {
		process_block_whirlpool(input);
		input += 128;
		len -= 128;
	}
	hash = (char *)malloc(128);
	ft_memcpy(hash, state, 128);
	return (hash);
}

void whirlpool_file(const char *filename, t_options *options) {
	int fd = open(filename, O_RDONLY);
	if (fd < 0) {
		perror(filename);
		return;
	}
	char buffer[1024];
	char *hash = NULL;
	size_t total_size = 0;
	while (1) {
		ssize_t bytes_read = read(fd, buffer, 128);
		if (bytes_read < 0) {
			perror("read");
			close(fd);
			return;
		}
		if (bytes_read == 0)
			break;
		total_size += bytes_read; // for the padding at the end
		if (hash)
			free(hash);
		hash = whirlpool(buffer, bytes_read, total_size);
	}
	if (options->q == 0)
	{
		if (options->r == 0)
		{
			print_hexa(hash, 64);
			write(1, "  ", 2);
			if (ft_strncmp(filename, "/dev/stdin", 10) == 0)
				write(1, "-", 1);
			else
				write(1, filename, ft_strlen(filename));
		}
		else
		{
			if (ft_strncmp(filename, "/dev/stdin", 10) == 0)
				write(1, "-", 1);
			else
				write(1, filename, ft_strlen(filename));
			write(1, "  ", 2);
			print_hexa(hash, 64);
		}
	}
	else
		print_hexa(hash, 64);
	free(hash);
}

void whirlpool_helper(t_options *options)
{
	t_string_list *current = options->strings;
	while (current)
	{
		if (current->from_file)
			whirlpool_file(current->str, options);
		else
		{
			size_t len = ft_strlen(current->str);
			char *hash = whirlpool(current->str, len, len);
			if (options->q == 0)
			{
				if (options->r == 0)
				{
					print_hexa(hash, 64);
					write(1, "  ", 2);
					write(1, "-", 1);
				}
				else
				{
					write(1, "-", 1);
					write(1, "  ", 2);
					print_hexa(hash, 64);
				}
			}
			else
				print_hexa(hash, 64);
			free(hash);
		}
		write(1, "\n", 1);
		current = current->next;
	}
}


